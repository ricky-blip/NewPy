print("----Operasi Tambah ( + )----")
a = 1 
b = 2
tambah = a + b
print(a, "+", b, "=", tambah)

print("----Operasi Kurang ( - )----")
c = 5 
d = 2
kurang = c - d
print(c, "-", d, "=", kurang)

print("----Operasi Kali ( * )----")
e = 10 
f = 2
kali = e * f
print(e, "*", f, "=", kali)

print("----Operasi Bagi ( / )----")
g = 20 
h = 7
bagi = g / h
print(g, "/", h, "=", bagi)

print("----Operasi Eksponen atau Pangkat ( ** )----")
i = 5 
j = 5
pangkat = i ** j
print(i, "**", j, "=", pangkat)