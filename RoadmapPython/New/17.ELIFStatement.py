# ELIF = else if Statement

number = int(input("Type Number You Like: "))

if number == 1: # condition 1
    print(f"Your Number is : {number}") # TRUE 1
elif number == 2: # condition 2
    print(f"Your Number is : {number}") # TRUE 2
elif number == 3: # condition 3
    print(f"Your Number is : {number}") # TRUE 3
else:
    print("WRONG NUMBER") # FALSE

print(f"Thanks User : {number}")
